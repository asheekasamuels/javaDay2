// Declare variables for name and surname
let firstName = "Asheeka";
let lastName = "Samuels";

// Concatenate the two variables
let fullName = firstName + " " + lastName;
console.log(fullName); // Output will be "Asheeka Samuels"

// Declare a variable for a number
let numberVar = 5;

// Multiply the variable by 5
console.log(numberVar * 5); // Output will be 25

